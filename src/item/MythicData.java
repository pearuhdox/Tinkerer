package item;

public class MythicData {

	String nbt;
	String description;

	public MythicData() {
		description = null;
		nbt = null;
	}

	public String getNbt() {
		return nbt;
	}

	public void setNbt(String nbt) {
		this.nbt = nbt;
	}

	public String getDescription() {
		return description;
	}

	public void setDescription(String description) {
		this.description = description;
	}
	
	
}